/**
 * 분석 유형에 따른 전체 시스템 프롬프트 생성
 */
export function getFullSystemPrompt(analysisType: string): string {
  const config = ANALYSIS_TYPES[analysisType];
  if (!config) {
    return BASE_PROMPT.getFullBasePrompt();
  }
  return `${BASE_PROMPT.getFullBasePrompt()}\n\n${config.systemInstruction}`;
}

/**
 * 분석 유형에 따른 태스크 인스트럭션 조회
 */
export function getTaskInstruction(analysisType: string): string {
  const config = ANALYSIS_TYPES[analysisType];
  return config?.taskInstruction || '';
}

/**
 * Perplexity 검색 타입에 따른 시스템 프롬프트 생성
 */
export function getPerplexitySystemPrompt(searchType: string): string {
  const config = PERPLEXITY_SEARCH_TYPES[searchType];
  if (!config) {
    return PERPLEXITY_BASE_PROMPT;
  }
  return `${PERPLEXITY_BASE_PROMPT}\n\n${config.systemInstruction}`;
}

